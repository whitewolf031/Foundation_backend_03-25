# 5. pip uninstall library_name - o'chirish
# 6. pip freeze - kutubxonalar ro'yxatini korish
# 7. pip freeze > requirements.txt - kutubxonalarni txt file ga yozish
# 8. pip install -r requirements.txt - bu file ni ichidagi hamma kutubxonalarni yuklash